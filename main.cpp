#include <iostream>
using namespace std;

int main() {
    // Khai báo 3 số nguyên
    int a = 5, b = 3, c = 2;

    // Tính tổng và hiệu
    int sum = a + b + c;
    int diff = a - b - c;

    // In ra kết quả
    cout << "Tong: " << sum << endl;
    cout << "Hieu: " << diff << endl;

    return 0;
}
